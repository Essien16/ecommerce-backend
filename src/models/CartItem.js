const mongoose = require('mongoose');

const cartItemSchema = new mongoose.Schema({
  productId: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'Product',
    required: true
  },
  quantity: {
    type: Number,
    required: true,
    min: 1
  },
  price: {
    type: Number,
    required: true,
    min: 0
  },
  cart: { 
    type: mongoose.Schema.Types.ObjectId, 
    ref: "Cart" 
},
  total: {
    type: Number,
    required: true,
    min: 0
  },
  createdAt: {
    type: Date,
    default: Date.now
  },
  updatedAt: {
    type: Date,
    default: Date.now
  },
  status: {
    type: String,
    enum: ['active', 'deleted', 'purchased'],
    default: 'active'
  }
});

const CartItem = mongoose.model('CartItem', cartItemSchema);

module.exports = CartItem;
